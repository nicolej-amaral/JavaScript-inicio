function duasVariaveis() {

    let = diferenca;
    let = dobroTriplo;
    let = multiplicacao; 

    let numero1 = prompt("Digite um número inteiro:");

    let numero2 = prompt("Digite outro número inteiro:");

    diferenca = numero1 - numero2;

    dobroTriplo = (2 * numero1) + (3 * numero2);

    multiplicacao = numero1 * numero2;

    alert("A diferença dos dois número é de " + diferenca);

    alert("A soma do dobro do promeiro número com o triplo do outro é de " + dobroTriplo);

    alert("A multiplicação entre os dois números é de " + multiplicacao);

};